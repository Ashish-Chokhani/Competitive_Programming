void solve(){
  ll n;
  cin>>n;
  vector<ll>arr(n);
  vector<int>where(n+1);
  for(int i=0;i<n;i++){
    cin>>arr[i];
    where[arr[i]]=i;
  }
  ll rounds=1;
  for(int collect=2;collect<=n;collect++){
  	if(where[collect]<where[collect-1]) rounds++;
  }
  cout<<rounds<<"\n";
} 

