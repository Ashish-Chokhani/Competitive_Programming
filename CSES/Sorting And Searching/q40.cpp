void solve(){
  ll n,k;
  cin>>n>>k;
  vector<pair<ll,ll>>arr(n);
  for(auto &x:arr){
    cin>>x.first>>x.second;
  }
  sort(arr.begin(),arr.end(),[](pair<ll,ll> &i1,pair<ll,ll> &i2){
  	return (i1.second<i2.second);
  });
  //Count the number of r strictly greater than cur_l
  multiset<ll>st;
  //for (ll i = 0; i < k; ++i) st.insert(0);
  ll ans=0;
  for(auto &x:arr){
  	debug(st);
  	if((ll)st.size()<k){
  		st.insert(x.second);
  		cout<<x.first<<" "<<x.second<<"\n";
  		ans++;
  		continue;
  	}
  	auto it=st.upper_bound(x.first);
  	if(it==st.begin()) continue;
  	st.erase(--it);
  	cout<<x.first<<" "<<x.second<<"\n";
  	st.insert(x.second);
  	ans++;
  }
  cout<<ans<<"\n";
} 

