void solve(){
  ll n;
  cin>>n;
  vector<pair<ll,pair<ll,ll>>>intervals(n);
  for(ll i=0;i<n;i++){
  	cin>>intervals[i].first>>intervals[i].second.first;
  	intervals[i].second.second=i;
  }
  sort(intervals.begin(),intervals.end());
  //debug(intervals);
  ll room=0,maxi=0;
  multiset<pair<ll,ll>>st;
  vector<ll>ans(n);
  for(ll i=0;i<n;i++){
  	if(st.empty()){
  		ll ind=intervals[i].second.second;
  		ans[ind]=++room;
  		maxi=max(maxi,room);
  		st.insert({intervals[i].second.first,room});
  		continue;
  	}
  	auto it=st.begin();
  	if((*it).first<intervals[i].first){
  		ll ind=intervals[i].second.second;
  		ans[ind]=(*it).second;
  		st.erase(it);
  		st.insert({intervals[i].second.first,ans[ind]});
  	}
  	else{
  		ll ind=intervals[i].second.second;
  		ans[ind]=++room;
  		maxi=max(maxi,room);
  		st.insert({intervals[i].second.first,room});
  	}
  }
  cout<<maxi<<"\n";
  for(auto it:ans){
  	cout<<it<<" ";
  }
  cout<<"\n";
} 

