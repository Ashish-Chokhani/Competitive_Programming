void solve(){
  ll n,x;
  cin>>n>>x;
  vector<ll>arr(n);
  for(auto &a:arr){
    cin>>a;
  }
  vector<ll>dp(x+1,0);
  dp[0]=1;
  for(ll i=1;i<=x;i++){
  	for(ll j=0;j<n;j++){
  		if(i-arr[j]>=0) dp[i]+=dp[i-arr[j]];
  		dp[i]%=mod;
  	}
  }
  cout<<dp[x]<<"\n";
} 

