void solve(){
  ll n,m;
  cin>>n>>m;
  vector<ll>adj[n];
  for(ll i=0;i<m;i++){
  	ll u,v;
  	cin>>u>>v;
  	--u;--v;
    adj[v].push_back(u);
  }
  vector<vector<ll>>dp((1LL<<n),vector<ll>(n,0));
  dp[1][0]=1;
  for(ll mask=0;mask<(1LL<<(n-1));mask++){
  	for(ll i=1;i<n-1;i++){
  		if(!((mask>>i)&1)) continue;
  		for(auto j:adj[i])
  			if((mask>>j)&1){
  				dp[mask][i]+=dp[mask^(1LL<<i)][j];
  				dp[mask][i]%=mod;
  			}
  	}
  }
  ll ans=0;
  for(auto par:adj[n-1]){
    ans+=dp[(1LL<<(n-1))-1][par];
    ans%=mod;
  }
  cout<<ans<<"\n";
} 

