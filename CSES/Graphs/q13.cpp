void solve(){
  ll n,m,k;
  cin>>n>>m>>k;
  vector<pair<ll,ll>>adj[n+1];
  for(ll i=0;i<m;i++){
  	ll u,v,wt;
  	cin>>u>>v>>wt;
  	--u;--v;
  	adj[u].push_back({v,wt});
  }
  vector<multiset<ll>>distance(n);
  for(ll i=0;i<n;i++){
  	for(ll j=1;j<=k;j++){
  		distance[i].insert(INF);
  	}
  }
  distance[0].insert(0);
  auto x=distance[0].end();
  --x;
  distance[0].erase(x);
  priority_queue<pair<ll,ll>,vector<pair<ll,ll>>,greater<pair<ll,ll>>>pq;
  pq.push({0,0});
  while(!pq.empty()){
  	ll dist=pq.top().first;
  	ll node=pq.top().second;
  	pq.pop();
  	if(*distance[node].rbegin()<dist) continue;
  	for(auto it:adj[node]){
  		ll to=it.first;
  		ll wt=it.second;
  		if(dist+wt<*distance[to].rbegin()){
  			distance[to].insert(dist+wt);
  			auto x=distance[to].end();
  			--x;
  			distance[to].erase(x);
  			pq.push({dist+wt,to});
  		}
  	}
  }
  for(auto it:distance[n-1]){
  	cout<<it<<" ";
  }
  cout<<"\n";
} 

