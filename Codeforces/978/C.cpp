int gcd(int a, int b) {
    if (a == 0)
        return b;
 
    return gcd(b % a, a);
}

int floor_div(int x, int y) {
    assert(y != 0);
    if (y < 0) {
        y = -y;
        x = -x;
    }
    if (x >= 0) return x / y;
    return (x + 1) / y - 1;
}

int ceil_div(int x, int y) {
    assert(y != 0);
    if (y < 0) {
        y = -y;
        x = -x;
    }
    if (x <= 0) return x / y;
    return (x - 1) / y + 1;
}

void solve(){
	int N;
	cin>>N;
	vector<vector<int>>grid(2,vector<int>(N+3));
	for(int j=0;j<2;j++){
		string S; cin>>S;
		for(int i=0;i<N;i++) grid[j][i+1]=(S[i]=='A');
	}
    auto getVal=[&](int i,int t,int p){
    	int cnt=0;
    	if(p==3) cnt+=grid[t][i]+grid[t][i+1]+grid[t][i+2];
    	else if(p==2) cnt+=grid[0][i]+grid[1][i]+grid[0][i+1];
    	else if(p==1) cnt+=grid[0][i]+grid[1][i]+grid[1][i+1];
        else if(p==4) cnt+=grid[0][i]+grid[1][i+1]+grid[0][i+1];
        else if(p==5) cnt+=grid[1][i]+grid[1][i+1]+grid[0][i+1];
    	return cnt>=2;
    };
    vector<vector<int>>dp(N+5,vector<int>(4));
    for(int i=N;i>=1;i--) for(int j=0;j<3;j++){
    	if(j==0){
    		dp[i][j]=max(dp[i][j],getVal(i,0,3)+getVal(i,1,3)+dp[i+3][0]);
    		dp[i][j]=max(dp[i][j],getVal(i,0,2)+dp[i+1][2]);
    		dp[i][j]=max(dp[i][j],getVal(i,0,1)+dp[i+1][1]);
        }
        else if(j==1){
        	dp[i][j]=max(dp[i][j],getVal(i,0,3)+getVal(i+1,1,3)+dp[i+3][j]);
    		dp[i][j]=max(dp[i][j],getVal(i,0,4)+dp[i+2][0]);
        }
        else if(j==2){
        	dp[i][j]=max(dp[i][j],getVal(i,1,3)+getVal(i+1,0,3)+dp[i+3][2]);
    		dp[i][j]=max(dp[i][j],getVal(i,0,5)+dp[i+2][0]);
        }
    }
    cout<<dp[1][0]<<endl;
} 

