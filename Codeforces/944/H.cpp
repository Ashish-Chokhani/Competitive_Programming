int floor_div(int x, int y) {
    assert(y != 0);
    if (y < 0) {
        y = -y;
        x = -x;
    }
    if (x >= 0) return x / y;
    return (x + 1) / y - 1;
}

int ceil_div(int x, int y) {
    assert(y != 0);
    if (y < 0) {
        y = -y;
        x = -x;
    }
    if (x <= 0) return x / y;
    return (x - 1) / y + 1;
}

int n;
vector<vector<int>> adj, adj_t;
vector<bool> used;
vector<int> order, comp;
vector<bool> assignment;

void dfs1(int v) {
    used[v] = true;
    for (int u : adj[v]) {
        if (!used[u])
            dfs1(u);
    }
    order.push_back(v);
}

void dfs2(int v, int cl) {
    comp[v] = cl;
    for (int u : adj_t[v]) {
        if (comp[u] == -1)
            dfs2(u, cl);
    }
}

bool solve_2SAT() {
    order.clear();
    used.assign(n, false);
    for (int i = 0; i < n; ++i) {
        if (!used[i])
            dfs1(i);
    }

    comp.assign(n, -1);
    for (int i = 0, j = 0; i < n; ++i) {
        int v = order[n - i - 1];
        if (comp[v] == -1)
            dfs2(v, j++);
    }

    assignment.assign(n / 2, false);
    for (int i = 0; i < n; i += 2) {
        if (comp[i] == comp[i + 1])
            return false;
        assignment[i / 2] = comp[i] > comp[i + 1];
    }
    return true;
}

void add_disjunction(int a, bool na, int b, bool nb) {
    a = 2*a ^ na;
    b = 2*b ^ nb;
    int neg_a = a ^ 1;
    int neg_b = b ^ 1;
    adj[neg_a].push_back(b);
    adj[neg_b].push_back(a);
    adj_t[b].push_back(neg_a);
    adj_t[a].push_back(neg_b);
}

void solve(){
  cin>>n;
  n=n*2;
  adj.clear();
  adj_t.clear();
  adj.resize(n);
  adj_t.resize(n);
  vector<vector<int>>matrix(3,vector<int>(n/2));
  for(int i=0;i<3;i++) for(int j=0;j<n/2;j++) cin>>matrix[i][j];
  for(int i=0;i<n/2;i++){
  	add_disjunction(abs(matrix[0][i])-1,matrix[0][i]<0,abs(matrix[1][i])-1,matrix[1][i]<0);
  	add_disjunction(abs(matrix[1][i])-1,matrix[1][i]<0,abs(matrix[2][i])-1,matrix[2][i]<0);
  	add_disjunction(abs(matrix[2][i])-1,matrix[2][i]<0,abs(matrix[0][i])-1,matrix[0][i]<0);
  }
  if(solve_2SAT()) cout<<"YES\n";
  else cout<<"NO\n";
} 

