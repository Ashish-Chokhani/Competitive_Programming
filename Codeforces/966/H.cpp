int gcd(int a, int b) {
    if (a == 0)
        return b;
 
    return gcd(b % a, a);
}

int floor_div(int x, int y) {
    assert(y != 0);
    if (y < 0) {
        y = -y;
        x = -x;
    }
    if (x >= 0) return x / y;
    return (x + 1) / y - 1;
}

int ceil_div(int x, int y) {
    assert(y != 0);
    if (y < 0) {
        y = -y;
        x = -x;
    }
    if (x <= 0) return x / y;
    return (x - 1) / y + 1;
}

template <typename num_t> 
struct segtree {
  int n, depth;
  vector<num_t> tree, lazy;

  void init(int s, long long* arr) {
    n = s;
    tree = vector<num_t>(4 * s, 0);
    lazy = vector<num_t>(4 * s, 0);
    init(0, 0, n - 1, arr);
  }

  num_t init(int i, int l, int r, long long* arr) {
    if (l == r) return tree[i] = arr[l];

    int mid = (l + r) / 2;
    num_t a = init(2 * i + 1, l, mid, arr),
          b = init(2 * i + 2, mid + 1, r, arr);
    return tree[i] = a.op(b);
  }

  void update(int l, int r, num_t v) {
	if (l > r) return;
    update(0, 0, n - 1, l, r, v);
  }

  num_t update(int i, int tl, int tr, int ql, int qr, num_t v) {
    eval_lazy(i, tl, tr);
	
	if (tr < ql || qr < tl) return tree[i];
    if (ql <= tl && tr <= qr) {
      lazy[i] = lazy[i].val + v.val;
      eval_lazy(i, tl, tr);
      return tree[i];
    }
    
    int mid = (tl + tr) / 2;
    num_t a = update(2 * i + 1, tl, mid, ql, qr, v),
          b = update(2 * i + 2, mid + 1, tr, ql, qr, v);
    return tree[i] = a.op(b);
  }

  num_t query(int l, int r) {
	if (l > r) return num_t::null_v;
    return query(0, 0, n-1, l, r);
  }

  num_t query(int i, int tl, int tr, int ql, int qr) {
    eval_lazy(i, tl, tr);
    
    if (ql <= tl && tr <= qr) return tree[i];
    if (tr < ql || qr < tl) return num_t::null_v;

    int mid = (tl + tr) / 2;
    num_t a = query(2 * i + 1, tl, mid, ql, qr),
          b = query(2 * i + 2, mid + 1, tr, ql, qr);
    return a.op(b);
  }

  void eval_lazy(int i, int l, int r) {
    tree[i] = tree[i].lazy_op(lazy[i], (r - l + 1));
    if (l != r) {
      lazy[i * 2 + 1] = lazy[i].val + lazy[i * 2 + 1].val;
      lazy[i * 2 + 2] = lazy[i].val + lazy[i * 2 + 2].val;
    }

    lazy[i] = num_t();
  }
};

struct max_t {
  long long val;
  static const long long null_v = -9223372036854775807LL;

  max_t(): val(0) {}
  max_t(long long v): val(v) {}

  max_t op(max_t& other) {
    return max_t(max(val, other.val));
  }
  
  max_t lazy_op(max_t& v, int size) {
    return max_t(val + v.val);
  }
};

const int N=1LL<<21;
int arr[N];
segtree<max_t>seg;

void solve(){
  int n;
  cin>>n;
  set<int>s{0,2*N};
  seg.update(1,1,2*N-1);
  auto insert=[&](int x){
  	auto it=s.insert(x).first;
  	auto l=prev(it),r=next(it);
  	seg.update(*l+1,*l+1,x-*l-1-seg.query(*l+1,*l+1).val);
  	seg.update(x+1,x+1,*r-x-1-seg.query(x+1,x+1).val);
  };
  auto remove=[&](int x){
  	auto it=s.find(x);
  	auto l=prev(it),r=next(it);
  	seg.update(*l+1,*l+1,*r-*l-1-seg.query(*l+1,*l+1).val);
  	seg.update(x+1,x+1,-seg.query(x+1,x+1).val);
  	s.erase(it);
  };
  auto find=[&](int k){
  	int l=0,r=N,ans=N;
  	while(r-l>1){
  		int mid=(l+r)>>1;
  		if(seg.query(l,mid).val>=k){
  			ans=min(ans,mid);
  			r=mid-1;
  		}
  		else l=mid;
  	}
  	return l;
  };
  for(int i=0;i<n;i++){
  	int a; cin>>a;
  	insert(a);
  }
  int m;
  cin>>m;
  while(m--){
  	char c; int x;
  	cin>>c>>x;
  	if(c=='+') insert(x);
  	else if(c=='-') remove(x);
  	else cout<<find(x)<<" \n"[m==0];
  }
  for (auto x : s) if (x < N) seg.update(x+1,x+1,-seg.query(x+1,x+1).val);  
} 


