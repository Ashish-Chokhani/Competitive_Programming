void solve(){
  ll n,k;
  cin>>n>>k;
  priority_queue<ll>pq;
  map<ll,ll>freq;
  for(ll bits=62;bits>=0;bits--){
  	if(n&(1LL<<bits)){
  		pq.push(bits);
  		freq[bits]++;
  	}
  }
  ll sz=pq.size();
  if(sz>k){
  	cout<<"NO\n";
  	return;
  }
  ll rem=k-sz;
  bool flag=false;
  while(rem--){
  	ll before=pq.top();
  	if(freq[before]>rem+1){
  		rem++;
  		flag=true;
  		break;
  	}
  	pq.pop();
  	freq[before]--;
  	pq.push(before-1);
  	pq.push(before-1);
  	freq[before-1]+=2;
  }
  if(flag){
  	priority_queue<ll,vector<ll>,greater<ll>>pqg;
  	while(!pq.empty()){
  		ll before=pq.top();
  		pq.pop();
  		pqg.push(before);
  	}
  	while(rem--){
  	ll before=pqg.top();
  	pqg.pop();
  	pqg.push(before-1);
  	pqg.push(before-1);
    }
    while(!pqg.empty()){
  		ll before=pqg.top();
  		pqg.pop();
  		pq.push(before);
  	}
  }
  cout<<"YES\n";
  while(!pq.empty()){
  	ll x=pq.top();
  	pq.pop();
  	cout<<x<<" \n"[pq.size()==0];
  }
} 

