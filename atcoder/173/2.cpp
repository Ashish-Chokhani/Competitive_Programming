// Custom hash function for pairs
struct pair_hash {
    template <class T1, class T2>
    std::size_t operator () (const std::pair<T1,T2> &pair) const {
        auto h1 = std::hash<T1>{}(pair.first);
        auto h2 = std::hash<T2>{}(pair.second);
        return h1 ^ h2;
    }
};

void solve(){
  ll n;
  cin>>n;
  vector<pair<ll,ll>>points(n);
  auto findSlope=[&](pair<ll,ll>a,pair<ll,ll>b)->pair<ll,ll>{
  	if(a.second>b.second) swap(a,b);
  	return {b.second-a.second,b.first-a.first};	
  };
  
  ll cnt=0;
  for(ll i=0;i<n;i++) cin>>points[i].first>>points[i].second;
  unordered_map<pair<ll,ll>,ll,pair_hash> mp;
  for(ll i=0;i<n;i++) for(ll j=0;j<n;j++){
  	if(i==j) continue;
  	pair<ll,ll>slope=findSlope(points[i],points[j]);
  	mp.clear();
  	for(ll k=0;k<n;k++){
  		ll a=slope.first;
  		ll b=slope.second;
      if(a==0) b=1;
      if(b==0) a=1;
  		ll x=points[k].first;
  		ll y=points[k].second;
      ll val=b*y-a*x;
  		mp[{val,b}]++;
  	}
  	for(auto it:mp) cnt=max(cnt,it.second);
  }
  cout<<min(n/3,n-cnt)<<endl;
} 

