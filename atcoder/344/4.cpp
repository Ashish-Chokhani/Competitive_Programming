void solve(){
	string s;
	cin>>s;
	ll l=s.length();
	s='#'+s;
	ll n;
	cin>>n;
	vector<string>S[n+1];
	for(ll i=1;i<=n;i++){
		ll m;
		cin>>m;
		for(ll j=0;j<m;j++){
			string t;
			cin>>t;
			S[i].push_back(t);
		}
	}
	auto doesSatisfy=[&](ll i,string t){
		ll k=i;
		for(ll j=t.length()-1;j>=0;j--){
			if(k<0) return false;
			if(s[k]!=t[j]) return false;
			k--;
		}
		return true;
	};
	vector<vector<ll>>dp(l+1,vector<ll>(n+1));
	for(ll i=0;i<=l;i++){  //Make all changes
		for(ll j=0;j<=n;j++){
			dp[i][j]=INF;
			if(i==0) dp[i][j]=0;
		}
	}
	for(ll i=1;i<=l;i++){
		for(ll j=1;j<=n;j++){
			for(string t:S[j]){
				if(doesSatisfy(i,t)){
				   dp[i][j]=min(dp[i][j],1+dp[i-t.length()][j-1]);
				   assert(i-t.length()>=0);
				}
			}
			dp[i][j]=min(dp[i][j],dp[i][j-1]);
			assert(j>=1);
		}
	}
	ll ans=dp[l][n];
	
	if(ans==INF) ans=-1;
	cout<<ans<<endl;
} 

