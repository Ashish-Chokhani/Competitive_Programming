int gcd(int a, int b) {
    if (a == 0)
        return b;
 
    return gcd(b % a, a);
}

int floor_div(int x, int y) {
    assert(y != 0);
    if (y < 0) {
        y = -y;
        x = -x;
    }
    if (x >= 0) return x / y;
    return (x + 1) / y - 1;
}

int ceil_div(int x, int y) {
    assert(y != 0);
    if (y < 0) {
        y = -y;
        x = -x;
    }
    if (x <= 0) return x / y;
    return (x - 1) / y + 1;
}

void solve(){
  int H,W,Q;
  cin>>H>>W>>Q;
  int ans=H*W;
  vector<set<int>>r(H+1),c(W+1);
  for(int i=1;i<=H;i++) for(int j=1;j<=W;j++){
    r[i].insert(j);
    c[j].insert(i);
  }
  vector<vector<int>>present(H+1,vector<int>(W+1,1));
  while(Q--){
    int R,C;
    cin>>R>>C;
    if(r[R].find(C)!=r[R].end()){
        ans--;
        r[R].erase(C);
        c[C].erase(R);
        continue;
    }
    auto x=r[R].lower_bound(C);
    if(x!=r[R].begin()){
        int b=*prev(x);
        ans--;
        r[R].erase(b);
        c[b].erase(R);
    }
    if(x!=r[R].end()){
        int b=*x;
        ans--;
        r[R].erase(b);
        c[b].erase(R);   
    }
    x=c[C].lower_bound(R);
    if(x!=c[C].begin()){
        int a=*prev(x);
        ans--;
        r[a].erase(C);
        c[C].erase(a);
    }
    if(x!=c[C].end()){
        int a=*x;
        ans--;
        r[a].erase(C);
        c[C].erase(a);
    }
  }
  cout<<ans<<endl;
} 

