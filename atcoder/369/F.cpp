int gcd(int a, int b) {
    if (a == 0)
        return b;
 
    return gcd(b % a, a);
}

int floor_div(int x, int y) {
    assert(y != 0);
    if (y < 0) {
        y = -y;
        x = -x;
    }
    if (x >= 0) return x / y;
    return (x + 1) / y - 1;
}

int ceil_div(int x, int y) {
    assert(y != 0);
    if (y < 0) {
        y = -y;
        x = -x;
    }
    if (x <= 0) return x / y;
    return (x - 1) / y + 1;
}

void solve(){
	int H,W,N;
	cin>>H>>W>>N;
	vector<array<int,2>>arr(N);
	for(int i=0;i<N;i++) cin>>arr[i][0]>>arr[i][1];
	sort(all(arr));
    vector<int>dp(N+1,INF),id(N+1,-1),par(N+1,-1);
    dp[0]=arr[0][1]; id[0]=0;
    int len=N-1;
    for(int i=1;i<N;i++){
    	int idx=upper_bound(all(dp),arr[i][1])-dp.begin();
    	dp[idx]=arr[i][1];
    	id[idx]=i;
    	par[i]=idx?id[idx-1]:-1;
    }
    
    while(id[len]==-1) len--;
    auto getPath=[&](int x1,int y1,int x2,int y2){
    	string s="";
    	while(x1>x2){
    		s+='D'; x1--;
    	}
    	while(y1>y2){
    		s+='R'; y1--;
    	}
    	return s;
    };
    int cur=id[len];
    string path="";
    path+=getPath(H,W,arr[cur][0],arr[cur][1]);
    while(cur!=-1 && par[cur]!=-1){
    	path+=getPath(arr[cur][0],arr[cur][1],arr[par[cur]][0],arr[par[cur]][1]);
    	cur=par[cur];
    }
    path+=getPath(arr[cur][0],arr[cur][1],1,1);
    reverse(all(path));
    cout<<len+1<<endl;
    cout<<path<<endl;
} 

