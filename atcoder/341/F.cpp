const ll maxN=5002;
vector<ll>adj[maxN];

void BFS(ll src,ll &cnt,vector<ll>&w,vector<ll>&A,vector<ll>&vis){
	queue<int>q;
	q.push(src);
	vis[src]=1;
	while(!q.empty()){
		ll node=q.front();
		q.pop();
		bool hasIncreased=false;
		for(auto it:adj[node]){
			if(w[it]<w[node]){
				A[it]+=A[node];
				hasIncreased=true;
			}
			if(vis[it]) continue;
			vis[it]=1;
			q.push(it);
		}
		if(hasIncreased) cnt+=A[node];
		A[node]=0;
	}
}

void solve(){
	ll n,m;
	cin>>n>>m;
	for(ll i=1;i<=m;i++){
		ll u,v;
		cin>>u>>v;
		--u;--v;
		adj[u].push_back(v);
		adj[v].push_back(u);
	}
	vector<ll>w(n),A(n);
	for(ll &x:w) cin>>x;
	for(ll &x:A) cin>>x;
	ll ans=0;
	for(ll i=5;i<6;i++){
		vector<ll>vis(n,0);
		vector<ll>A1=A;
		ll cnt=0;
		//What about mulitiple components
		for(ll j=0;j<n;j++){ //Change this later
		    if(vis[i]) continue;
		    BFS(i,cnt,w,A1,vis);
		}
		ans=max(ans,cnt);
	}
	cout<<ans<<endl;
} 

